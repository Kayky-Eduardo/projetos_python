def Soma(a, b):
    soma = a + b
    return soma

def Subtracao(a, b):
    subtracao = a - b
    return subtracao

def Produto(a, b):
    produto = a * b
    return produto

def Divisao(a, b):
    divisao = a / b
    return divisao

def Divisao_inteira(a, b):
    divisao_inteira = a // b
    return divisao_inteira

def Resto_da_divisao(a, b):
    resto_da_divisao = a % b
    return resto_da_divisao
